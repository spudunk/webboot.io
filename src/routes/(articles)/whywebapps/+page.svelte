<h2>The tradeoffs</h2>
<p>
	The only real potential tradeoffs are initial cost and availability of people who can work on it.
	Web apps aren’t something that can be built and managed by your average local Wordpress designer.
	To build a web app well a developer needs to understand how websites work at their core and how
	their framework works, on top of all the skills a Wordpress designer has.
</p>
<p>
	Fortunately making the equivalent of a Wordpress website is trivial for the folks who have taken
	the time to acquire these skills, and you aren't limited to expensive plugins and a slow platform.
	Plus, developers that are fantastic with these modern tools are abundantly available across the
	globe and in many cases willing to work for less than you might expect. Building a web app may be
	marginally more expensive, but the performance and feature options just aren’t available to
	traditional websites.
</p>

