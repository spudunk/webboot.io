<h3>Promotion</h3>
<p>This is where things get interesting and profitable.&nbsp;</p>
<p>
	You&rsquo;ll have a powerful, traffic hungry, sales generating monster rumbling behind the gate of
	your web host, waiting for an influx of content consumers to come eat up your content and
	offers.&nbsp;
</p>
<p>
	Let&rsquo;s get someone to go there, yeah? Sounds pretty smart, but a lot of businesses just
	squeeze this in as a final step without much thought. They just throw up a super unappealing ad
	(pun intended) that drops the viewer on the homepage! The <em>first</em> point of contact is the
	<em>last</em> thing these &ldquo;digital agencies&rdquo; consider.&nbsp;
</p>
<p>Your research and plan gets put into use again.&nbsp;</p>
<p>
	The home and about pages are like a phone book listing, people only find it if they look you
	up.&nbsp;
</p>
<p>
	The rest of the website is a salesman, connecting the leads from your ads to the content
	they&rsquo;re interested in, showing them products or services to help, and giving them an
	offer.&nbsp;
</p>
<p>What&rsquo;s important -&nbsp;</p>
<ol>
	<li>The ad that gets them in</li>
	<li>The content that makes them like and trust you</li>
	<li>The offer that makes them want to give you their money</li>
</ol>
<p>
	Directing ads to your homepage is a waste of money. You&rsquo;ll get leads, but your cost per lead
	will be really high.&nbsp;
</p>
<p>
	The big marketing agencies have collected creepy amounts of data from everyone to create <strong
		>unique advertising profiles</strong
	>
	for each of us. If you&rsquo;re marketing online you need to leverage it to be profitable. You do that
	with <strong>targeted marketing</strong>.
</p>
<p>There are two categories here:&nbsp;</p>
<ol>
	<li><strong>Product or service ads</strong> - attract people ready to buy</li>
	<li><strong>Content ads</strong> - attract people interested but not ready to buy</li>
</ol>
<p>
	Product or service ads direct to <strong>landing pages</strong>. Each one is written based on the
	ad as a solution and great offer. You can have multiple landing pages per ad and see which ones
	perform the best.&nbsp;
</p>
<p>
	Content ads can direct to <strong>opt-in pages</strong> where people provide their email for access
	to the content. If the content is helpful and interesting, they&rsquo;ll probably open an email from
	you with more content.&nbsp;
</p>
<p>
	Content ads can also direct to <strong>public content</strong> that they don&rsquo;t need to provide
	their email to read. After the content you can offer them opt-in content via email.&nbsp;
</p>
<p>
	Create content that addresses the problems your customers have. Help them work toward solving
	their problems. When they&rsquo;re ready to buy a solution they&rsquo;ll go to you.&nbsp;
</p>
<p>This setup will bring in warm leads immediately, and warm up cold leads over time.&nbsp;</p>
<p>What about the homepage?&nbsp;</p>
<ul>
	<li>It&rsquo;s there (not diminishing the importance of it)</li>
	<li>It provides a high level view of your business</li>
	<li>It reiterates your message and offer</li>
	<li>It directs traffic</li>
</ul>
