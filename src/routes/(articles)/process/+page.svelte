<h3>Research &amp; Plan</h3>
<p>
	Research the customer to understand what drives their purchasing decisions. Understand the barrier
	between them and your product or service, then write an offer they&rsquo;ll jump at.&nbsp;
</p>
<p>
	Collect the keywords, pain points, and wishes of your customer (from reviews). Study the
	alternative offers. Take notes.&nbsp;
</p>
<p>Do a lot of writing before starting the design.</p>
<ol>
	<li>Offers - that get people to contact you and buy</li>
	<li>Ads - that your customers will want to click on</li>
	<li>Content - to entertain and help your customer so them like your brand</li>
</ol>
<p>
	Integrate keywords and SEO structure, put together graphics and media, then finally&hellip; start
	designing a website.&nbsp;
</p>
