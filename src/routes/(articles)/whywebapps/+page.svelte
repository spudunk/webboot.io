<h2 class="font-bold mt-6">The difference between websites and web apps</h2>
<p>
	<strong>Websites</strong> are a set of interconnected web pages. They don’t need Javascript programming
	but support it for adding interactivity. The fundamental structure of websites remains the same as
	it has since the 90s.
</p>
<p>
	<strong>Web apps</strong> take a different approach. Instead of loading seperate documents for each
	page, they deliver a Javascript application that displays the content in a more powerful way. It does
	everything websites do but also improves performance and user experience. With web apps you really
	get the best of both worlds.
</p>
<p class="mt-6">
	While websites still do what they are intended to do and can serve a business well, the problem is
	that they are limited. Web apps have limits too (like push notifications on iPhone), but they are
	much more versatile than websites, sitting much closer to the abilities of native apps (like you
	install from the app store) than websites.
</p>

<p>If your business wants to implement features like these, there really isn’t a choice:</p>
<ul>
	<li>client accounts</li>
	<li>chats</li>
	<li>games</li>
	<li>user generated content</li>
	<li>dynamic content based on user preferences</li>
	<li>integrate with a CRM or APIs</li>
</ul>
<p>
	Web apps give you complete control over what each individual user sees, like how Facebook and
	Instagram feeds are different for each user. It’s also worth mentioning that web apps are super
	fast. The frameworks are designed to handle much more difficult tasks while maintaining good
	performance, so for most businesses the performance is overkill. This really benefits SEO.
</p>

